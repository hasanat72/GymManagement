public class Member {
    String name;
    String fathersname;
    String date;
    String contact;
    String email;
    String bloodgroup;
    String bp;
    String diabetics;

    public Member(String name, String fathersname, String date, String contact, String email, String bloodgroup, String bp, String diabetics) {
        this.name = name;
        this.fathersname = fathersname;
        this.date = date;
        this.contact = contact;
        this.email = email;
        this.bloodgroup = bloodgroup;
        this.bp = bp;
        this.diabetics = diabetics;
    }
    
  
}
